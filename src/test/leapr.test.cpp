void checkSab( const std::vector<double>& correctSab, 
  const std::vector<std::vector<std::vector<double>>>& sab ){

  int i = 0;
  for ( auto a1 : sab ){
    for ( auto a2 : a1 ){
      for ( auto a3 : a2 ){
        if ( i == int(correctSab.size()) ){ return; }
        REQUIRE( a3 == Approx(correctSab[i]).epsilon(1e-6) );
        i += 1;
      }
    }
  }
}





